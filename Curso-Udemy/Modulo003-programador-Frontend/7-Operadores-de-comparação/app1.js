/* 
 OS operadores de comparação são eles:

 ==  igualdade
 === exatamente igual
 != Diferente
!== exatamente diferente
> maior que
< menor que
>= maior ou igual que
<= menor ou igual que
*/

console.log("==, ===");
console.log("10 == 10", 10 == 10);
console.log("'10' == 10", "10" == 10);
console.log(" '10' === 10", "10" === 10);
console.log("========================================================");
console.log("!=, !==");
console.log("10 != 10", 10 != 10);
console.log("10 !== 10.1", 10 !== 10.1);
console.log("========================================================");
console.log(">,<,<=,>=");
console.log("10 > 10", 10 > 10);
console.log("10 < 10", 10 < 10);
console.log("10 >= 10", 10 >= 10);
console.log("10 <= 10", 10 <= 10);
console.log("========================================================");

// javascript é case sensitive
console.log("a == a: ", "a" == "a");
